namespace FlockWise.Core.Enums;

public enum SheepType
{
    CullEwe, // A ewe who is sold for meat as no longer fit for breeding.
    Ewe, // A female sheep who has given birth.
    DraftEwe, // A ewe who is too old for rough grazing on upland or hill and is moved to gentler grazing on the lowland.
    Tup, // A male sheep who has not yet been castrated.
    Teaser // A male sheep who has been castrated.
}